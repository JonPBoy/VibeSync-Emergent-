// Helper function to get styles by category
export const getStylesByCategory = (category) => {
  if (category === 'all') return MOCK_STYLES;
  return MOCK_STYLES.filter(style => style.category === category);
};

// Helper function to get random styles
export const getRandomStyles = (count = 5) => {
  const shuffled = [...MOCK_STYLES].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};
